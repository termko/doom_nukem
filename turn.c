#include "main.h"

void	turn_right(t_dm *wf)
{
	wf->pl->angle += 0.5;
	if (wf->pl->angle < 0)
		wf->pl->angle += 360;
}

void	turn_left(t_dm *wf)
{
	wf->pl->angle -= 0.5;
	if (wf->pl->angle >= 360)
		wf->pl->angle -= 360;
}
